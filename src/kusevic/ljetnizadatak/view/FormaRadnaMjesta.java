    @Override
    protected void ucitaj() {

        ucitajRadnike();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Objekt");
        model.addColumn("Odjel");
        model.addColumn("Naziv");
        tablicaMjesta.setModel(model);
        listaRadnoMjesto = HibernateUtil.getSession().createQuery("FROM RadnoMjesto WHERE obrisan=false").list();
        listaRadnoMjesto.forEach((s) -> {
            model.addRow(new Object[]{s, s.getOdjel(), s.getNaziv()});
        });
        tablicaMjesta.getColumnModel().getColumn(0).setWidth(0);
        tablicaMjesta.getColumnModel().getColumn(0).setMinWidth(0);
        tablicaMjesta.getColumnModel().getColumn(0).setMaxWidth(0);

    }

