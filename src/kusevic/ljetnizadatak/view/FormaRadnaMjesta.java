    private void ucitajRadnike() {

        if (radniciMjesta == null || radniciMjesta.isEmpty()) {

            DefaultListModel<Radnik> modelMjesta = new DefaultListModel<>();
            lstRadniciNaMjestu.setModel(modelMjesta);
        } else {
            DefaultListModel<Radnik> modelMjesta = new DefaultListModel<>();
            lstRadniciNaMjestu.setModel(modelMjesta);
            for (Radnik a : radniciMjesta) {
                {
                    modelMjesta.addElement(a);
                }
            }
        }

        if (radniciMjesta == null || radniciMjesta.isEmpty()) {
            DefaultListModel<Radnik> modelOstalih = new DefaultListModel<>();

            lstRadnici.setModel(modelOstalih);
            for (Radnik svaki : sviRadnici) {
                modelOstalih.addElement(svaki);
            }

        } else {
            DefaultListModel<Radnik> modelOstalih = new DefaultListModel<>();

            lstRadnici.setModel(modelOstalih);
            boolean naden = false;
            for (Radnik svaki : sviRadnici) {
                naden = false;
                for (Radnik izMjesta : radniciMjesta) {
                    if (svaki.getSifra().equals(izMjesta.getSifra())) {

                        naden = true;
                        break;
                    }

                }

                if (!naden) {
                    modelOstalih.addElement(svaki);

                }

            }

        }

    }

