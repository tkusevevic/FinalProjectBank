    private void btnUnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnosActionPerformed

        if (Provjera.provjeriJelText(tekstOdjel.getText()) == false) {
            greskoOdjel.setText("ERROR");
        }
        if (Provjera.provjeriJelText(tekstNaziv.getText()) == false) {
            greskoNaziv.setText("ERROR");
        }
        if (Provjera.provjeriJelText(tekstNaziv.getText()) == true && Provjera.provjeriJelText(tekstOdjel.getText()) == true) {
            entitet = new RadnoMjesto();

            spremi();
            ucitaj();
        }


    }//GEN-LAST:event_btnUnosActionPerformed
